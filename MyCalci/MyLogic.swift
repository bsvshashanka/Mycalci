import Foundation
public class Calculator{
    public init(){}
    public static func add(x: Int, y: Int)->Int{return x+y}
    public static func subtract(x: Int, y:Int)->Int{return x-y}
    public static func multiply(x: Int, y: Int)->Int{return x*y}
    public static func divide(x: Int, y: Int)->Int{return x/y}
}
